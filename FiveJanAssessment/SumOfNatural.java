package com.FiveJanAssessment;

public class SumOfNatural {
//Java program to calculate sum of natural numbers from 1 to 1000
	public static void main(String[] args) {
		int sum=0;
		for(int i=0;i<=1000;i++)
		{
			sum=sum+i;
		}
		System.out.println("sum of natural Number upto 1000 is :"+sum);

	}

}
